public class Equipo {
    String idEquipo;
    String equNombre;
    String equEscudo;
    String equDescripcion;
    String equUrl;
    String equEmail;
    String equContacto;
    String equContactoFono;
    String equContactoCelular;
    String equContactoEmail;
    String equCreacion;

    public Equipo() {
        this.idEquipo = idEquipo;
        this.equNombre = equNombre;
        this.equEscudo = equEscudo;
        this.equDescripcion = equDescripcion;
        this.equUrl = equUrl;
        this.equEmail = equEmail;
        this.equContacto = equContacto;
        this.equContactoFono = equContactoFono;
        this.equContactoCelular = equContactoCelular;
        this.equContactoEmail = equContactoEmail;
        this.equCreacion = equCreacion;
    }

    public String getIdEquipo() {
        return idEquipo;
    }

    public void setIdEquipo(String idEquipo) {
        this.idEquipo = idEquipo;
    }

    public String getEquNombre() {
        return equNombre;
    }

    public void setEquNombre(String equNombre) {
        this.equNombre = equNombre;
    }

    public String getEquEscudo() {
        return equEscudo;
    }

    public void setEquEscudo(String equEscudo) {
        this.equEscudo = equEscudo;
    }

    public String getEquDescripcion() {
        return equDescripcion;
    }

    public void setEquDescripcion(String equDescripcion) {
        this.equDescripcion = equDescripcion;
    }

    public String getEquUrl() {
        return equUrl;
    }

    public void setEquUrl(String equUrl) {
        this.equUrl = equUrl;
    }

    public String getEquEmail() {
        return equEmail;
    }

    public void setEquEmail(String equEmail) {
        this.equEmail = equEmail;
    }

    public String getEquContacto() {
        return equContacto;
    }

    public void setEquContacto(String equContacto) {
        this.equContacto = equContacto;
    }

    public String getEquContactoFono() {
        return equContactoFono;
    }

    public void setEquContactoFono(String equContactoFono) {
        this.equContactoFono = equContactoFono;
    }

    public String getEquContactoCelular() {
        return equContactoCelular;
    }

    public void setEquContactoCelular(String equContactoCelular) {
        this.equContactoCelular = equContactoCelular;
    }

    public String getEquContactoEmail() {
        return equContactoEmail;
    }

    public void setEquContactoEmail(String equContactoEmail) {
        this.equContactoEmail = equContactoEmail;
    }

    public String getEquCreacion() {
        return equCreacion;
    }

    public void setEquCreacion(String equCreacion) {
        this.equCreacion = equCreacion;
    }
}


